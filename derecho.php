      <p class="text-center">Los contratos informáticos son acuerdos de voluntades entre partes para la prestación de servicios o comercialización de bienes relacionados con la informática. Tienen características propias y únicas, como ser atípicos, lo que significa que no están regulados específicamente por la ley pero pueden existir legalmente a través de la libertad de contratación. También, las partes involucradas suelen ser identificadas como proveedores de bienes y servicios informáticos y usuarios. <br><br>

                            Determinar si un contrato informático es de naturaleza mercantil o civil puede ser complicado. Por ejemplo, si un estudiante contrata la paquetería básica de Microsoft Office, el proveedor (Microsoft Corporation) busca lucro, mientras que el estudiante simplemente necesita el software para uso personal. La decisión depende de si se considera el acto de comercio una especulación subjetiva o un intercambio de bienes o servicios con fines de lucro. Los tribunales han establecido criterios para determinar si un acto es de naturaleza comercial. <br><br>Estos criterios incluyen la intervención de un sujeto comerciante, la intermediación en el intercambio de bienes y la intención de obtener un lucro. Sin embargo, también se reconoce que la especulación comercial es subjetiva y depende de las intenciones individuales de las partes involucradas.</p>
